namespace RezRouting.Options
{
    /// <summary>
    /// Specifies changes to letter case when formatting a string value
    /// </summary>
    public enum CaseStyle
    {
        /// <summary>
        /// No case change
        /// </summary>
        None,
        /// <summary>
        /// Value converted to lower case
        /// </summary>
        Lower,
        /// <summary>
        /// Value converted to upper case
        /// </summary>
        Upper
    }
}